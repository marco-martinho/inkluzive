<h3 class="aboutintro">About inkluzive</h3> 
	<div class="aboutinfo">
		<p>inkluzive has a fundamental commitment to the games industry and the community it serves. We want to build deep, long-lasting relationships with our readers and advertisers by producing quality products that operate with the highest possible creative and commercial integrity.</p><br />
		<p>inkluzive's core editorial values are impartiality, respect, accuracy, fairness and transparency. To ensure we maintain those journalistic standards, we abide by a strict code of ethics - a set of rules that we live by, and procedures that we follow:</p>
	</div>

<div id="accordion"> 

	<h2>Advertising and editorial</h2>
    <div>
		<p>Advertising and editorial departments are completely separate.</p>
		<p>We report stories as we see them, as accurately and fairly as possible – but also with a point of view. We believe that our readers and advertisers are best served with editorial that is consistently reliable, fair and impartial.</p>
	</div>
	
	<h2>Publication</h2>
	<div>
		<p>If we receive a response after publication that changes the context or bearing of a story, we'll update that story or add a new one where necessary.</p>
	    <p>We always offer a right to reply, correct any factual errors brought to our attention, and credit our sources clearly where applicable.</p>
	</div>	
	
	<h2>Transparency</h2>
	<div>
		<p>We do not publish unsubstantiated rumors without first having attempted to verify them ourselves. We will respect any request for sources to remain confidential.</p>	
		<p>We believe in being fair, honest and transparent in our dealings with advertisers.</p>
	</div>
	
	<h2>Sales</h2>
	<div>
		<p>We will not 'oversell' nor over-claim our readership. Third parties regularly verify the traffic on all Gamer Network sites, and we publish that information promptly.</p>
		<p>We provide a consultative sales approach based on understanding our readers, our advertisers and the marketplace.</p>		
	</div>
	


	<h2>Events</h2>
	<div>
		<p>If we attend any kind of "VIP" event at any location outside of our offices, we will report on it with full transparency. We believe the experience and conditions of the event to be as editorially relevant as the product being covered.</p>
		<p>If we accept travel or accommodation from a company, we’ll disclose it.
		We will only accept games, items for reviews and things that enable us to do our job (i.e. consoles, peripherals).</p>
	</div>	
	
	<h2>Reviews</h2>
	<div>
		<p>Games and other items received through work may not be sold or traded. If they are no longer useful for work then they will be donated to local charities.</p>
		<p>Staff and contributors are not permitted to do "mock reviews" for, or provide consultancy to publishers.</p>
		<p>Staff and contributors may not write about a company they have worked for in any capacity within the last two years.</p>
	</div>
